package calculator;

public class Calculator { // public class calculator안에 main과 그냥 class를 둘 수도 있
    public int add(int i, int j) {
        return i + j;
    }
    public int subtract(int i, int j) {
        return i - j;
    }
    public int multiply(int i, int j) { return i * j; }
    public int divide(int i, int j) { return i / j; }

}
